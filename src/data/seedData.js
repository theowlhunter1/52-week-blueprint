export function getAllTasks(data) {
  const tasks = [];
  for (const quarter of data.quarters) {
    for (const block of quarter.blocks) {
      tasks.push(...block.tasks);
    }
  }
  return tasks;
}

export function getTaskById(data, taskId) {
  for (const quarter of data.quarters) {
    for (const block of quarter.blocks) {
      const task = block.tasks.find(t => t.id === taskId);
      if (task) return task;
    }
  }
  return null;
}

